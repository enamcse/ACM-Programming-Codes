/* character type data */
#include <stdio.h>

int main()
{
    char a, b, c, d, e;

    a = 65;      /* ASCII value for character 'A' */
    b = 120;     /* ASCII value for character 'x' */
    c = 51;      /* ASCII value for character '3' */
    d = 63;      /* ASCII value for character '?' */
    e = 32;      /* ASCII value for character ' ' (space) */

    printf("a = %c\nb = %c\nc = %c\nd = %c\ne = %c\n", a, b, c, d, e);

    return 0;
}
