class GameOfStones {
public:
	int count(vector <int> stones) {
		int n = stones.size(), sum=stones[0], now, ans=0;
		bool flag = true;
		for (int i = 1; i<n; i++)
        {
            if(stones[i]!=stones[i-1]) flag = false;
            sum+=stones[i];
        }
		if(flag) return 0;
		if(sum%n) return -1;
		now = sum/n;

		for (int i = 0; i<n; i++)
        {
            if(abs(now-stones[i])%2) return -1;
            ans+=(abs(now-stones[i])/2);
        }


		return ans/2;
	}
};


