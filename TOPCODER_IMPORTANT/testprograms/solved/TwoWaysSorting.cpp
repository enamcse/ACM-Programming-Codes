class TwoWaysSorting {
public:
	string sortingMethod(vector <string> stringList) {
		int len = stringList.size(), k = stringList[0].size();
		bool flag=true, flag2 = true;
		for (int i=1; i<len; i++)
        {
            if(stringList[i].size()<k) flag = false;
            if(comp(stringList[i-1],stringList[i])) flag2 = false;
            k = stringList[i].size();
        }
        if(flag&&flag2) return "both";
        else if(flag) return "lengths";
        else if(flag2) return "lexicographically";
        else return "none";
	}
	bool comp(string a, string b)
	{
	    return !(a<b);
	}
};


